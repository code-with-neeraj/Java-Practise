// package chapter2;

public class CWH_ch2_Operators {
    public static void main(String[] args) {
        int a = 5;
        int b = 6 % a;
        System.out.println(b);
        // Comparison Operators
        System.out.println(6==7);
        // Logical operators
        System.out.println(64>5 && 64>8);
        System.out.println(64<5 || 64>8);
        
    }
}
